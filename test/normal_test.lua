-- Tests for normal.lua (the coroutine interpreter). Run: luajit test/normal_test.lua
package.path = "vendor/lust/?.lua;./?.lua;" .. package.path

local lust   = require("lust")
local buffer = require("buffer")
local normal = require("normal")
local editor = require("editor")
local ex     = require("ex")
local sys    = require("sys")
local describe, it, expect = lust.describe, lust.it, lust.expect

-- Build an editor state with a live interpreter coroutine, primed to getkey.
local function make(text)
  local ed = editor.new_ed()
  ed.buf = buffer.new(text)
  ed.interp = coroutine.create(function() normal.loop(ed) end)
  assert(coroutine.resume(ed.interp))
  return ed
end

-- Feed a string of keys and let the coroutine drain them.
local function feed(ed, s)
  for i = 1, #s do ed.inject[#ed.inject + 1] = s:byte(i) end
  assert(coroutine.resume(ed.interp))
  return ed
end

local ESC = "\27"

describe("normal-mode interpreter", function()
  describe("motions", function()
    it("h j k l 0 $ move the cursor", function()
      local ed = make("abc\ndef")
      feed(ed, "ll"); expect(ed.cx).to.equal(3)
      feed(ed, "0");  expect(ed.cx).to.equal(1)
      feed(ed, "$");  expect(ed.cx).to.equal(3)
      feed(ed, "j");  expect(ed.cy).to.equal(2)
      feed(ed, "k");  expect(ed.cy).to.equal(1)
    end)
    it("honors a count", function()
      local ed = make("abcdef")
      feed(ed, "3l"); expect(ed.cx).to.equal(4)
    end)
    it("G goes to a line or the last line", function()
      local ed = make("a\nb\nc\nd")
      feed(ed, "G");  expect(ed.cy).to.equal(4)
      feed(ed, "2G"); expect(ed.cy).to.equal(2)
    end)
    it("w b e move by word within a line", function()
      local ed = make("foo bar baz")
      feed(ed, "w"); expect(ed.cx).to.equal(5)   -- start of 'bar'
      feed(ed, "e"); expect(ed.cx).to.equal(7)   -- end of 'bar'
      feed(ed, "b"); expect(ed.cx).to.equal(5)   -- back to start of 'bar'
    end)
  end)

  describe("find motions f t F T ; ,", function()
    it("f moves to the char; ; and , repeat", function()
      local ed = make("a.b.c")
      feed(ed, "f."); expect(ed.cx).to.equal(2)
      feed(ed, ";"); expect(ed.cx).to.equal(4)
      feed(ed, ","); expect(ed.cx).to.equal(2)
    end)
    it("t moves till before the char", function()
      local ed = make("xxx.y")
      feed(ed, "t."); expect(ed.cx).to.equal(3)
    end)
    it("F searches backward", function()
      local ed = make("a.b.c")
      feed(ed, "$F."); expect(ed.cx).to.equal(4)
    end)
    it("df deletes through the char (inclusive)", function()
      local ed = make("hello")
      feed(ed, "dfl"); expect(ed.buf:line(1)).to.equal("lo")
    end)
  end)

  describe("gg and marks", function()
    it("G then gg", function()
      local ed = make("a\nb\nc\nd")
      feed(ed, "G"); expect(ed.cy).to.equal(4)
      feed(ed, "gg"); expect(ed.cy).to.equal(1)
    end)
    it("dgg deletes to the first line (linewise)", function()
      local ed = make("a\nb\nc")
      feed(ed, "jdgg")                       -- from line 2, delete lines 1..2
      expect(ed.buf:get()).to.equal({ "c" })
    end)
    it("sets and jumps to a mark exactly (`)", function()
      local ed = make("hello\nworld")
      feed(ed, "ma"); feed(ed, "jl")
      expect(ed.cy).to.equal(2)
      feed(ed, "`a")
      expect(ed.cy).to.equal(1); expect(ed.cx).to.equal(1)
    end)
    it("'mark jumps to the mark's line", function()
      local ed = make("one\ntwo\nthree")
      feed(ed, "jma"); feed(ed, "G")         -- mark a at line 2, cursor to line 3
      feed(ed, "'a"); expect(ed.cy).to.equal(2)
    end)
  end)

  describe("UTF-8", function()
    local E = "\195\169" -- 'é', 2 bytes, 1 cell
    it("h and l move by character across multibyte", function()
      local ed = make("a" .. E .. "b")
      feed(ed, "l"); expect(ed.cx).to.equal(2)   -- onto é
      feed(ed, "l"); expect(ed.cx).to.equal(4)   -- skipped é's 2 bytes, onto b
      feed(ed, "h"); expect(ed.cx).to.equal(2)   -- back onto é
    end)
    it("x deletes a whole multibyte char", function()
      local ed = make("a" .. E .. "b")
      feed(ed, "lx")
      expect(ed.buf:line(1)).to.equal("ab")
    end)
    it("inserts a multibyte char typed as its bytes", function()
      local ed = make("ab")
      feed(ed, "i" .. E .. "\27")
      expect(ed.buf:line(1)).to.equal(E .. "ab")
    end)
  end)

  describe("| (goto column)", function()
    it("moves to a display column", function()
      local ed = make("abcdef")
      feed(ed, "4|"); expect(ed.cx).to.equal(4)
      feed(ed, "|"); expect(ed.cx).to.equal(1)   -- no count -> column 1
    end)
  end)

  describe("cword (word under cursor)", function()
    it("returns the word class run around the cursor, else empty", function()
      local ed = make("foo bar_baz, qux")
      ed.cx = 5;  expect(normal.cword(ed)).to.equal("bar_baz")  -- _ is a word char
      ed.cx = 1;  expect(normal.cword(ed)).to.equal("foo")
      ed.cx = 4;  expect(normal.cword(ed)).to.equal("")         -- on the space
      ed.cx = 12; expect(normal.cword(ed)).to.equal("")         -- on the comma
      ed.cx = 14; expect(normal.cword(ed)).to.equal("qux")
    end)
    it("captures a whole multibyte word", function()
      local ed = make("héllo wörld")
      ed.cx = 1; expect(normal.cword(ed)).to.equal("héllo")
    end)
  end)

  describe("gj / gk (screen-line motion)", function()
    it("gj == j when wrap is off", function()
      local ed = make("a\nb\nc")               -- no opts -> nowrap
      feed(ed, "gj"); expect(ed.cy).to.equal(2)
    end)
    it("gj/gk move by sub-row within a wrapped line", function()
      local ed = make("abcdefgh\nxyz")
      ed.opts = { wrap = true, tabstop = 8 }; ed.cols = 4  -- "abcdefgh" -> abcd/efgh
      feed(ed, "gj")                                       -- into the 2nd sub-row
      expect(ed.cy).to.equal(1); expect(ed.cx).to.equal(5) -- 'e'
      feed(ed, "gj")                                       -- across to the next line
      expect(ed.cy).to.equal(2); expect(ed.cx).to.equal(1)
      feed(ed, "gk")                                       -- back up one screen row
      expect(ed.cy).to.equal(1); expect(ed.cx).to.equal(5)
    end)
  end)

  describe("cross-line word motion", function()
    it("w crosses to the next line", function()
      local ed = make("foo\nbar")
      feed(ed, "w"); expect(ed.cy).to.equal(2); expect(ed.cx).to.equal(1)
    end)
    it("b crosses to the previous line", function()
      local ed = make("foo\nbar")
      feed(ed, "j0b"); expect(ed.cy).to.equal(1)
    end)
    it("dw on the last word of a line stops at the newline", function()
      local ed = make("foo\nbar")
      feed(ed, "dw"); expect(ed.buf:get()).to.equal({ "", "bar" })
    end)
  end)

  describe("operator + motion composition", function()
    it("dw deletes to the next word", function()
      local ed = make("foo bar")
      feed(ed, "dw")
      expect(ed.buf:line(1)).to.equal("bar")
      expect(ed.regs['"'].text).to.equal("foo ")
    end)
    it("d$ deletes to end of line (inclusive)", function()
      local ed = make("hello")
      feed(ed, "lld$")                            -- from col 3
      expect(ed.buf:line(1)).to.equal("he")
    end)
    it("dd / 2dd delete whole lines (linewise)", function()
      local ed = make("a\nb\nc\nd")
      feed(ed, "dd")
      expect(ed.buf:get()).to.equal({ "b", "c", "d" })
      feed(ed, "2dd")
      expect(ed.buf:get()).to.equal({ "d" })
      expect(ed.regs['"'].linewise).to.be(true)
    end)
  end)

  describe("text objects", function()
    it("diw deletes the word under the cursor (from within)", function()
      local ed = make("foo bar baz")
      feed(ed, "wdiw")                              -- cursor on 'bar'
      expect(ed.buf:line(1)).to.equal("foo  baz")
      expect(ed.regs['"'].text).to.equal("bar")
    end)
    it("daw includes the trailing blank", function()
      local ed = make("foo bar baz")
      feed(ed, "wdaw")
      expect(ed.buf:line(1)).to.equal("foo baz")
    end)
    it("daw on the last word takes the leading blank instead", function()
      local ed = make("foo bar")
      feed(ed, "wdaw")                              -- no trailing blank -> leading
      expect(ed.buf:line(1)).to.equal("foo")
    end)
    it("diw on whitespace deletes the blank run", function()
      local ed = make("foo   bar")
      feed(ed, "llldiw")                            -- cursor on first space
      expect(ed.buf:line(1)).to.equal("foobar")
    end)
    it("daw on whitespace adds the following word", function()
      local ed = make("foo   bar baz")
      feed(ed, "llldaw")
      expect(ed.buf:line(1)).to.equal("foo baz")
    end)
    it("ciw replaces the word and leaves insert applied", function()
      local ed = make("foo bar")
      feed(ed, "wciwX" .. ESC)
      expect(ed.buf:line(1)).to.equal("foo X")
    end)
    it("diW treats punctuation as part of the WORD", function()
      local ed = make("foo.bar baz")
      feed(ed, "diW")
      expect(ed.buf:line(1)).to.equal(" baz")
      expect(ed.regs['"'].text).to.equal("foo.bar")
    end)
    it("di( / da( from inside the parens", function()
      local ed = make("a(bcd)e")
      feed(ed, "lldi(")                             -- cursor on 'b'
      expect(ed.buf:line(1)).to.equal("a()e")
      local ed2 = make("a(bcd)e")
      feed(ed2, "llda(")
      expect(ed2.buf:line(1)).to.equal("ae")
    end)
    it("di( works with the cursor on either bracket", function()
      local ed = make("a(bcd)e")
      feed(ed, "ldi(")                              -- on '('
      expect(ed.buf:line(1)).to.equal("a()e")
      local ed2 = make("a(bcd)e")
      feed(ed2, "5ldi(")                            -- on ')'
      expect(ed2.buf:line(1)).to.equal("a()e")
    end)
    it("di{ picks the innermost enclosing pair", function()
      local ed = make("{a{b}c}")
      feed(ed, "3ldi{")                             -- cursor on 'b'
      expect(ed.buf:line(1)).to.equal("{a{}c}")
    end)
    it("ci( on empty parens opens insert between them", function()
      local ed = make("f()")
      feed(ed, "lci(X" .. ESC)                      -- cursor on '('
      expect(ed.buf:line(1)).to.equal("f(X)")
    end)
    it("di( spanning lines splices the inner range", function()
      local ed = make("foo(\nbar\n)baz")
      feed(ed, "3ldi(")                             -- on '(' at col 4
      expect(ed.buf:get()).to.equal({ "foo()baz" })
    end)
    it("di\" / da\" operate on the quoted string", function()
      local ed = make('say "hi" ok')
      feed(ed, '5ldi"')                             -- cursor inside the quotes
      expect(ed.buf:line(1)).to.equal('say "" ok')
      local ed2 = make('say "hi" ok')
      feed(ed2, '5lda"')                            -- + trailing blank
      expect(ed2.buf:line(1)).to.equal("say ok")
    end)
    it("ci\" before a string operates on the next pair", function()
      local ed = make('x "ab"')
      feed(ed, 'ci"Z' .. ESC)                       -- cursor at col 1, before the pair
      expect(ed.buf:line(1)).to.equal('x "Z"')
    end)
    it("dip / dap operate linewise on the paragraph", function()
      local ed = make("a\nb\n\nc")
      feed(ed, "dip")
      expect(ed.buf:get()).to.equal({ "", "c" })
      expect(ed.regs['"'].linewise).to.be(true)
      local ed2 = make("a\nb\n\nc")
      feed(ed2, "dap")                              -- + the trailing blank line
      expect(ed2.buf:get()).to.equal({ "c" })
    end)
    it("a failed object is a clean no-op (no insert, no change)", function()
      local ed = make("hello")
      feed(ed, "ci(")                               -- no parens on the line
      expect(ed.mode).to.equal("normal")            -- did NOT fall into insert
      feed(ed, "x")                                 -- next command still normal-mode
      expect(ed.buf:line(1)).to.equal("ello")
    end)
    it(". repeats a text-object change at the new cursor", function()
      local ed = make("foo bar baz qux")
      feed(ed, "daw")                               -- delete 'foo '
      expect(ed.buf:line(1)).to.equal("bar baz qux")
      feed(ed, ".")                                 -- repeat -> delete 'bar '
      expect(ed.buf:line(1)).to.equal("baz qux")
    end)
  end)

  describe("custom text objects (:textobj filter)", function()
    it("applies a charwise range from the filter's stdout", function()
      local ed = make("hello")
      ex.dispatch(ed, [[textobj z sh -c 'printf "char 1 1 1 3\n"']])
      feed(ed, "diz")                               -- filter says cols 1..3
      expect(ed.buf:line(1)).to.equal("lo")
    end)
    it("applies a linewise range", function()
      local ed = make("a\nb\nc")
      ex.dispatch(ed, [[textobj z sh -c 'printf "line 1 2\n"']])
      feed(ed, "diz")
      expect(ed.buf:get()).to.equal({ "c" })
      expect(ed.regs['"'].linewise).to.be(true)
    end)
    it("c on a custom object enters insert (proves the coroutine path)", function()
      local ed = make("hello")
      ex.dispatch(ed, [[textobj z sh -c 'printf "char 1 1 1 3\n"']])
      feed(ed, "cizXY" .. ESC)                      -- delete cols 1..3, then type
      expect(ed.buf:line(1)).to.equal("XYlo")
    end)
    it("empty filter output is a clean no-op (no change, no insert)", function()
      local ed = make("hello")
      ex.dispatch(ed, [[textobj z sh -c ':']])      -- prints nothing
      feed(ed, "diz")
      expect(ed.mode).to.equal("normal")
      feed(ed, "x"); expect(ed.buf:line(1)).to.equal("ello")
    end)
    it("a builtin object always wins over a registered key", function()
      local ed = make("foo bar")
      ex.dispatch(ed, [[textobj w sh -c 'printf "char 1 1 1 5\n"']])
      feed(ed, "diw")                               -- builtin word, NOT the filter
      expect(ed.buf:line(1)).to.equal(" bar")
    end)
    it("textobj KEY with no command unregisters it", function()
      local ed = make("hello")
      ex.dispatch(ed, [[textobj z sh -c 'printf "char 1 1 1 3\n"']])
      ex.dispatch(ed, "textobj z")                  -- clear
      feed(ed, "diz")
      expect(ed.buf:line(1)).to.equal("hello")      -- z now unhandled: no-op
    end)
    -- End-to-end with the shipped contrib/lvi-textobj-tag (assumes repo-root cwd,
    -- which `make test` and a direct `luajit test/...` both use). Proves the
    -- script's stdout format matches what ex.textobj_range parses.
    it("contrib/lvi-textobj-tag drives dit / cit", function()
      local ed = make("<p>hello</p>")
      ex.dispatch(ed, "textobj t contrib/lvi-textobj-tag")
      feed(ed, "4ldit")                             -- cursor inside 'hello'
      expect(ed.buf:line(1)).to.equal("<p></p>")
      local ed2 = make("<p>hello</p>")
      ex.dispatch(ed2, "textobj t contrib/lvi-textobj-tag")
      feed(ed2, "4lcitHi" .. ESC)                   -- change inner, type "Hi"
      expect(ed2.buf:line(1)).to.equal("<p>Hi</p>")
    end)
  end)

  describe("case operators (gu / gU / g~)", function()
    it("gUiw uppercases the word", function()
      local ed = make("foo bar")
      feed(ed, "gUiw"); expect(ed.buf:line(1)).to.equal("FOO bar")
    end)
    it("guiw lowercases the word", function()
      local ed = make("FOO BAR")
      feed(ed, "guiw"); expect(ed.buf:line(1)).to.equal("foo BAR")
    end)
    it("g~iw toggles case", function()
      local ed = make("FoO bar")
      feed(ed, "g~iw"); expect(ed.buf:line(1)).to.equal("fOo bar")
    end)
    it("gU$ works over a charwise motion from the cursor", function()
      local ed = make("foo bar")
      feed(ed, "wgU$"); expect(ed.buf:line(1)).to.equal("foo BAR")
    end)
    it("gUU uppercases the whole current line", function()
      local ed = make("hi there")
      feed(ed, "gUU"); expect(ed.buf:line(1)).to.equal("HI THERE")
    end)
    it("gUip uppercases a whole paragraph (multi-line)", function()
      local ed = make("foo\nbar\n\nx")
      feed(ed, "gUip"); expect(ed.buf:get()).to.equal({ "FOO", "BAR", "", "x" })
    end)
    it("gg still works as a motion (g namespace intact)", function()
      local ed = make("a\nb\nc")
      feed(ed, "G");  expect(ed.cy).to.equal(3)
      feed(ed, "gg"); expect(ed.cy).to.equal(1)
    end)
    it("dgg still deletes to the first line (operator + g motion)", function()
      local ed = make("a\nb\nc")
      feed(ed, "Gdgg"); expect(ed.buf:get()).to.equal({ "" })
    end)
  end)

  describe("! filter operator and gq", function()
    it("! seeds the prompt with the motion's line range and filters", function()
      local ed = make("charlie\nalpha\nbravo")
      feed(ed, "!Gsort\r")                          -- filter lines 1..3 through sort
      expect(ed.buf:get()).to.equal({ "alpha", "bravo", "charlie" })
    end)
    it("!! filters the current line", function()
      local ed = make("hello\nworld")
      feed(ed, "!!tr a-z A-Z\r")
      expect(ed.buf:get()).to.equal({ "HELLO", "world" })
    end)
    it("cancelling the ! prompt leaves the buffer unchanged", function()
      local ed = make("b\na")
      feed(ed, "!Gsort\27")                          -- Esc cancels
      expect(ed.buf:get()).to.equal({ "b", "a" })
    end)
    it("gq filters a paragraph through $LVI_FMT", function()
      sys.setenv("LVI_FMT", "tr a-z A-Z")
      local ed = make("foo\nbar\n\nbaz")
      feed(ed, "gqip"); expect(ed.buf:get()).to.equal({ "FOO", "BAR", "", "baz" })
    end)
    it(". repeats gq on the next paragraph", function()
      sys.setenv("LVI_FMT", "tr a-z A-Z")
      local ed = make("foo\n\nbar")
      feed(ed, "gqip"); expect(ed.buf:line(1)).to.equal("FOO")
      feed(ed, "G.");   expect(ed.buf:line(3)).to.equal("BAR")
    end)
  end)

  describe("insert-mode Tab and expandtab", function()
    it("inserts a literal tab by default", function()
      local ed = make("x")
      feed(ed, "A\t\27")
      expect(ed.buf:line(1)).to.equal("x\t")
    end)
    it("expands to spaces by one shiftwidth when expandtab is on", function()
      local ed = make("")
      ex.dispatch(ed, "set et sw=4")                  -- shiftwidth, not tabstop, is the indent unit
      feed(ed, "i\t\27")                              -- from col 1
      expect(ed.buf:line(1)).to.equal("    ")         -- 4 spaces
    end)
    it("is column-aware: fills only to the next shiftwidth boundary", function()
      local ed = make("ab")
      ex.dispatch(ed, "set et sw=4")
      feed(ed, "A\t\27")                              -- cursor at display col 2 -> 2 spaces
      expect(ed.buf:line(1)).to.equal("ab  ")
    end)
    it("uses shiftwidth even when tabstop differs", function()
      local ed = make("")
      ex.dispatch(ed, "set et sw=4 ts=8")             -- the exact ~/.lvirc case
      feed(ed, "i\t\27")
      expect(ed.buf:line(1)).to.equal("    ")         -- 4 (sw), not 8 (ts)
    end)
    it("with autoindent, indentation propagates as spaces", function()
      local ed = make("")
      ex.dispatch(ed, "set et sw=4 ai")
      feed(ed, "i\tfoo\rbar\27")                      -- tab->spaces, then <CR> carries them
      expect(ed.buf:get()).to.equal({ "    foo", "    bar" })
    end)
  end)

  describe("autoindent", function()
    it("is off by default: o does not carry indent", function()
      local ed = make("    hello")
      feed(ed, "obar\27")
      expect(ed.buf:get()).to.equal({ "    hello", "bar" })
    end)
    it("o / O inherit the current line's indent when ai is on", function()
      local ed = make("    hello")
      ex.dispatch(ed, "set ai")
      feed(ed, "obelow\27")
      expect(ed.buf:get()).to.equal({ "    hello", "    below" })
      feed(ed, "Oabove\27")                          -- opens above line 2 (the new one)
      expect(ed.buf:line(2)).to.equal("    above")
    end)
    it("<CR> carries the indent onto the continuation", function()
      local ed = make("    foo")
      ex.dispatch(ed, "set ai")
      feed(ed, "A\rbar\27")                          -- append, newline, type
      expect(ed.buf:get()).to.equal({ "    foo", "    bar" })
    end)
    it("trims an autoindent-only line on <Esc>", function()
      local ed = make("    foo")
      ex.dispatch(ed, "set ai")
      feed(ed, "o\27")                               -- open, type nothing, leave
      expect(ed.buf:get()).to.equal({ "    foo", "" })
    end)
    it("<CR> on an autoindent-only line leaves it empty (no trailing blanks)", function()
      local ed = make("    foo")
      ex.dispatch(ed, "set ai")
      feed(ed, "A\r\rbar\27")
      expect(ed.buf:get()).to.equal({ "    foo", "", "    bar" })
    end)
    it("set ai? queries and ai! toggles", function()
      local ed = make("x")
      expect((ex.dispatch(ed, "set ai?"))).to.equal("noautoindent")
      ex.dispatch(ed, "set autoindent")
      expect((ex.dispatch(ed, "set ai?"))).to.equal("autoindent")
      ex.dispatch(ed, "set ai!")
      expect((ex.dispatch(ed, "set ai?"))).to.equal("noautoindent")
    end)
  end)

  describe(":[range]normal (per-line normal-mode keys)", function()
