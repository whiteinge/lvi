  describe("insert-mode completion (Ctrl-P/Ctrl-N)", function()
    -- Stub the completer: record the context core hands over, return a canned word.
    local function with_completer(ed, ret)
      local seen = {}
      ed.hooks = { complete = { "stub" } }
      ed.complete_run = function(cmd, token, left, dir)
        seen.cmd, seen.token, seen.left, seen.dir = cmd, token, left, dir
        return ret
      end
      return seen
    end
    local CP, CN = string.char(16), string.char(14)

    it("replaces the token before the cursor with the completer's word", function()
      local ed = make("")
      local seen = with_completer(ed, "foobar")
      feed(ed, "ifoo" .. CP)
      expect(ed.buf:line(1)).to.equal("foobar")
      expect(ed.cx).to.equal(7)                 -- cursor after the inserted word
      expect(seen.token).to.equal("foo")        -- the non-blank run before cursor
      expect(seen.dir).to.equal("prev")         -- Ctrl-P
    end)

    it("extracts the token as the non-blank run and preserves text after the cursor", function()
      local ed = make("fooXYZ")
      local seen = with_completer(ed, "foobar")
      feed(ed, "llli" .. CP)                     -- cursor on 'X', insert before it
      expect(seen.token).to.equal("foo")
      expect(seen.left).to.equal("foo")
      expect(ed.buf:line(1)).to.equal("foobarXYZ")
    end)

    it("Ctrl-N passes dir=next", function()
      local ed = make("")
      local seen = with_completer(ed, "x")
      feed(ed, "ia" .. CN)
      expect(seen.dir).to.equal("next")
    end)

    it("an empty completion result leaves the buffer unchanged", function()
      local ed = make("")
      with_completer(ed, "")
      feed(ed, "ifoo" .. CP)
      expect(ed.buf:line(1)).to.equal("foo")
      expect(ed.cx).to.equal(4)
    end)

    it("is a no-op with no completer registered", function()
      local ed = make("")
      feed(ed, "ifoo" .. CP .. "bar")            -- Ctrl-P ignored; typing continues
      expect(ed.buf:line(1)).to.equal("foobar")
    end)
  end)

  describe("jumplist (Ctrl-O / Ctrl-I)", function()
    local CO, CI = "\15", "\9"                    -- Ctrl-O, Ctrl-I (Tab)
    it("Ctrl-O walks back over jumps and Ctrl-I forward", function()
      local ed = make("l1\nl2\nl3\nl4\nl5")
      feed(ed, "G");  expect(ed.cy).to.equal(5)   -- jump from line 1
      feed(ed, "2G"); expect(ed.cy).to.equal(2)   -- jump from line 5
      feed(ed, CO);   expect(ed.cy).to.equal(5)   -- back to the line 2 jump's origin
      feed(ed, CO);   expect(ed.cy).to.equal(1)   -- back to the G jump's origin
      feed(ed, CO);   expect(ed.cy).to.equal(1)   -- nothing older: stays put
      feed(ed, CI);   expect(ed.cy).to.equal(5)   -- forward again
      feed(ed, CI);   expect(ed.cy).to.equal(2)   -- back to the live edge (line 2)
      feed(ed, CI);   expect(ed.cy).to.equal(2)   -- nothing newer: stays put
    end)
    it("non-jump motions do not push, and Ctrl-O/I are no-ops when empty", function()
      local ed = make("l1\nl2\nl3\nl4\nl5")
      feed(ed, "jjj"); expect(ed.cy).to.equal(4)  -- j is not jump-class
      feed(ed, CO);    expect(ed.cy).to.equal(4)  -- empty jumplist: no move
      feed(ed, CI);    expect(ed.cy).to.equal(4)
    end)
    it("dedups by line so revisiting a line doesn't bloat the list", function()
      local ed = make("l1\nl2\nl3\nl4\nl5")
      feed(ed, "G");   feed(ed, "1G")             -- jump to 5, then back to 1
      feed(ed, "G")                               -- jump to 5 again (origin line 1 dup)
      expect(#ed.jumps.list).to.equal(2)          -- {5, 1}, not {1, 5, 1}
    end)
  end)
