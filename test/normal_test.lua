    it("jumps from a close bracket back to its match", function()
      local ed = make("a(bcd)e")
      feed(ed, "f)%")
      expect(ed.cx).to.equal(2)   -- back to '('
    end)
    it("searches forward on the line when not on a bracket", function()
      local ed = make("foo (bar) baz")
      feed(ed, "%")               -- cursor at col 1, first bracket is '(' at 5
      expect(ed.cx).to.equal(9)   -- its ')'
    end)
    it("honors nesting of the same type", function()
      local ed = make("(a(b)c)")
      feed(ed, "%")
      expect(ed.cx).to.equal(7)   -- outer ')'
    end)
    it("matches across lines", function()
      local ed = make("foo(\n  bar\n)baz")
      feed(ed, "f(%")
      expect(ed.cy).to.equal(3); expect(ed.cx).to.equal(1)
    end)
    it("is a no-op when the line has no bracket", function()
      local ed = make("plain text")
      feed(ed, "%")
      expect(ed.cy).to.equal(1); expect(ed.cx).to.equal(1)
    end)
    it("d% deletes inclusively across the pair", function()
      local ed = make("x(abc)y")
      feed(ed, "f(d%")
      expect(ed.buf:line(1)).to.equal("xy")
    end)
  end)

  describe("sentence motions ( )", function()
    it(") moves to the next sentence (2 spaces, then EOL)", function()
      -- "One.  Two.  Three" : starts at cols 1, 7, 13
      local ed = make("One.  Two.  Three")
      feed(ed, ")");  expect(ed.cx).to.equal(7)
      feed(ed, ")");  expect(ed.cx).to.equal(13)
    end)
    it("( moves back to the sentence start", function()
      local ed = make("One.  Two.  Three")
      feed(ed, "$");  expect(ed.cx).to.equal(17)
      feed(ed, "(");  expect(ed.cx).to.equal(13)
      feed(ed, "(");  expect(ed.cx).to.equal(7)
      feed(ed, "(");  expect(ed.cx).to.equal(1)
    end)
    it("does NOT break on a single space (POSIX two-space rule)", function()
      local ed = make("a. b. c")   -- single spaces -> one sentence on the line
      feed(ed, ")")
      expect(ed.cy).to.equal(1); expect(ed.cx).to.equal(7)  -- stops at EOF (last char)
    end)
    it("treats a terminator at end-of-line as a break", function()
      local ed = make("Done.\nNext one")
      feed(ed, ")")
      expect(ed.cy).to.equal(2); expect(ed.cx).to.equal(1)
    end)
    it("skips closing quotes/brackets before the break", function()
      local ed = make('He said "hi."  Bye')   -- .")  two spaces -> start at "Bye"
      feed(ed, ")")
      expect(ed.cx).to.equal(16)   -- 'B' of Bye
    end)
    it("treats a blank line as a sentence boundary", function()
      local ed = make("first\n\nsecond")
      feed(ed, ")");  expect(ed.cy).to.equal(2)   -- the empty line
      feed(ed, ")");  expect(ed.cy).to.equal(3); expect(ed.cx).to.equal(1)
    end)
    it("honors a count", function()
      local ed = make("One.  Two.  Three")
      feed(ed, "2)"); expect(ed.cx).to.equal(13)
    end)
    it("d) deletes the sentence charwise-exclusive", function()
      local ed = make("One.  Two.  Three")
      feed(ed, "d)")
      expect(ed.buf:line(1)).to.equal("Two.  Three")
    end)
  end)

  describe("shift operators < >", function()
    it(">> indents by shiftwidth, tab-optimized by default", function()
      local ed = make("foo\nbar")
      feed(ed, ">>")                       -- default sw=8, ts=8, noexpandtab
      expect(ed.buf:line(1)).to.equal("\tfoo")
      expect(ed.cx).to.equal(2)            -- first non-blank
    end)
    it("<< removes a level of indent", function()
      local ed = make("\tfoo")
      feed(ed, "<<")
      expect(ed.buf:line(1)).to.equal("foo")
    end)
    it("respects :set shiftwidth and expandtab", function()
      local ed = make("foo")
      feed(ed, ":set sw=2 expandtab\r")
      feed(ed, ">>")
      expect(ed.buf:line(1)).to.equal("  foo")
      feed(ed, ">>")
      expect(ed.buf:line(1)).to.equal("    foo")
    end)
    it("2>> shifts a count of lines", function()
      local ed = make("a\nb\nc")
      feed(ed, ":set sw=2 et\r")
      feed(ed, "2>>")
      expect(ed.buf:get()).to.equal({ "  a", "  b", "c" })
    end)
    it(">j shifts the lines a motion spans", function()
      local ed = make("a\nb\nc")
      feed(ed, ":set sw=2 et\r")
      feed(ed, ">j")
      expect(ed.buf:get()).to.equal({ "  a", "  b", "c" })
    end)
    it("leaves an empty line unchanged on right shift", function()
      local ed = make("foo\n\nbar")
      feed(ed, ":set sw=2 et\r")
      feed(ed, "2>j")   -- shift 3 lines (current + 2 down)
      expect(ed.buf:get()).to.equal({ "  foo", "", "  bar" })
    end)
    it("a whole multi-line shift undoes as one change", function()
      local ed = make("a\nb\nc")
      feed(ed, ":set sw=2 et\r")
      feed(ed, "2>>")
      feed(ed, "u")
      expect(ed.buf:get()).to.equal({ "a", "b", "c" })
    end)
  end)

  describe("R (replace/overwrite mode)", function()
    it("overwrites characters in place", function()
      local ed = make("hello")
      feed(ed, "RXYZ" .. ESC)
      expect(ed.buf:line(1)).to.equal("XYZlo")
    end)
    it("extends the line when typing past end-of-line", function()
      local ed = make("ab")
      feed(ed, "RXYZW" .. ESC)
      expect(ed.buf:line(1)).to.equal("XYZW")
    end)
    it("backspace moves left without restoring", function()
      local ed = make("hello")
      feed(ed, "RXY\127Z" .. ESC)   -- type XY, backspace over Y, type Z
      expect(ed.buf:line(1)).to.equal("XZllo")
    end)
    it("leaves the cursor stepped back on <Esc>, like insert", function()
      local ed = make("hello")
      feed(ed, "RAB" .. ESC)
      expect(ed.buf:line(1)).to.equal("ABllo")
      expect(ed.cx).to.equal(2)
      expect(ed.mode).to.equal("normal")
    end)
    it("is repeatable with .", function()
      local ed = make("aaaa\nbbbb")
      feed(ed, "RXX" .. ESC)
      expect(ed.buf:line(1)).to.equal("XXaa")
      feed(ed, "j0.")
      expect(ed.buf:line(2)).to.equal("XXbb")
    end)
  end)

  describe("screen-position motions H M L", function()
    local function tall()
      local lines = {}
      for i = 1, 30 do lines[i] = "line" .. i end
      local ed = make(table.concat(lines, "\n"))
      ed.rows = 11; ed.top = 5   -- textrows = 10, showing lines 5..14
      return ed
    end
    it("H goes to the top of the screen (+count)", function()
      local ed = tall()
      feed(ed, "H");  expect(ed.cy).to.equal(5)
      feed(ed, "3H"); expect(ed.cy).to.equal(7)
    end)
    it("L goes to the bottom of the screen (-count)", function()
      local ed = tall()
      feed(ed, "L");  expect(ed.cy).to.equal(14)
      feed(ed, "2L"); expect(ed.cy).to.equal(13)
    end)
    it("M goes to the middle of the screen", function()
      local ed = tall()
      feed(ed, "M"); expect(ed.cy).to.equal(9)   -- (5 + 14) / 2
    end)
    it("dH deletes linewise up to the top of the screen", function()
      local ed = tall()
      feed(ed, "7GdH")   -- from line 7 up to top (line 5): delete 5,6,7
      expect(ed.buf:line(5)).to.equal("line8")
    end)
    it("L/M stay on-screen when a top line wraps", function()
      -- line 1 is 25 cols wide; at W=10 it spans 3 screen rows.
      local ed = make(string.rep("x", 25) .. "\na\nb\nc\nd")
      ed.opts = { wrap = true, tabstop = 8 }
      ed.rows = 5; ed.cols = 10; ed.top = 1; ed.topsub = 0
      -- 4 text rows = line1's 3 rows + line2 -> only lines 1 and 2 are visible.
      feed(ed, "H"); expect(ed.cy).to.equal(1)
      feed(ed, "L"); expect(ed.cy).to.equal(2)   -- not line 3 (the old buggy result)
      feed(ed, "M"); expect(ed.cy).to.equal(1)
    end)
  end)

  describe("z reposition and Ctrl-G", function()
    local function tall()
      local lines = {}
      for i = 1, 30 do lines[i] = "L" .. i end
      local ed = make(table.concat(lines, "\n"))
      ed.rows = 11   -- textrows = 10
      return ed
    end
    it("z<CR> puts the current line at the top", function()
      local ed = tall()
      feed(ed, "15G"); feed(ed, "z\r")
      expect(ed.top).to.equal(15); expect(ed.cy).to.equal(15)
    end)
    it("z. centers the current line", function()
      local ed = tall()
      feed(ed, "15Gz.")
      expect(ed.top).to.equal(10)  -- 15 - floor(10/2)
    end)
    it("z- puts the current line at the bottom", function()
      local ed = tall()
      feed(ed, "15Gz-")
      expect(ed.top).to.equal(6)   -- 15 - (10 - 1)
    end)
    it("[count]z<CR> repositions around a given line", function()
      local ed = tall()
      feed(ed, "20z\r")
      expect(ed.top).to.equal(20); expect(ed.cy).to.equal(20)
    end)
    it("zt puts the current line at the top, keeping the column", function()
      local ed = tall()
      feed(ed, "15Glzt")                          -- line 15, one col right, then zt
      expect(ed.top).to.equal(15); expect(ed.cy).to.equal(15)
      expect(ed.cx).to.equal(2)                   -- column kept (z<CR> -> first non-blank = 1)
    end)
    it("zz centers the current line, keeping the column", function()
      local ed = tall()
      feed(ed, "15Gllzz")
      expect(ed.top).to.equal(10)                 -- 15 - floor(10/2)
      expect(ed.cy).to.equal(15); expect(ed.cx).to.equal(3)
    end)
    it("zb puts the current line at the bottom, keeping the column", function()
      local ed = tall()
      feed(ed, "15Glzb")
      expect(ed.top).to.equal(6)                  -- 15 - (10 - 1)
      expect(ed.cy).to.equal(15); expect(ed.cx).to.equal(2)
    end)
    it("[count]zt keeps the column across the line change", function()
      local ed = tall()
      feed(ed, "l20zt")                           -- col 2 on line 1, then 20zt
      expect(ed.top).to.equal(20); expect(ed.cy).to.equal(20)
      expect(ed.cx).to.equal(2)
    end)
    it("zz centers a wrapping line by SCREEN rows, not buffer lines (wrap on)", function()
      -- 9 lines each wrapping into 2 screen rows at cols=4. Centering the last
      -- line (textrows 10 -> 5 rows up) must count screen rows: buffer-line math
      -- would wrongly land at top=4, and refresh() would then pin it to the
      -- bottom (the reported wrap bug). Screen-row math lands at top=6,topsub=1.
      local ed = make(("abcdef\n"):rep(9))
      ed.opts = { wrap = true, tabstop = 8 }; ed.cols = 4; ed.rows = 11
      feed(ed, "Gzz")
      expect(ed.cy).to.equal(9)
      expect(ed.top).to.equal(6); expect(ed.topsub).to.equal(1)
      editor.refresh(ed)                          -- the driver's clamp must not undo it
      expect(ed.top).to.equal(6); expect(ed.topsub).to.equal(1)
    end)
    it("Ctrl-G reports file position on the message line", function()
      local ed = tall()
      feed(ed, "15G\7")
      expect(ed.message).to.exist()
      expect(ed.message:find("line 15 of 30", 1, true)).to.exist()
    end)
  end)

  describe("ZZ / ZQ", function()
    it("ZQ quits without writing", function()
      local ed = make("dirty")
      feed(ed, "x")                 -- make it modified
      expect(ed.buf.modified).to.be(true)
      feed(ed, "ZQ")
      expect(ed.running).to.be(false)
    end)
    it("ZZ on a modified unnamed buffer errors instead of quitting", function()
      local ed = make("hello")
      feed(ed, "x")                 -- modify it: :x must write, but there is no path
      expect(ed.buf.modified).to.be(true)
      feed(ed, "ZZ")
      expect(ed.running).to.be(true)
      expect(ed.message).to.exist()
    end)
    it("ZZ on a clean unnamed buffer quits without writing", function()
      local ed = make("hello")   -- unmodified, so :x skips the write and just quits
      feed(ed, "ZZ")
      expect(ed.running).to.be(false)
    end)
  end)

  describe("the ':' prompt shares ex.dispatch", function()
    it("runs an ex command typed at ':'", function()
